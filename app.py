#!/data/data/com.termux/files/usr/bin/python3

import os
import sys
import subprocess

def install_dependencies():
    """Instala todas as depend√™ncias automaticamente"""
    print("üîß Instalando depend√™ncias necess√°rias...")
    commands = [
        'pkg update -y',
        'pkg upgrade -y',
        'pkg install -y python ffmpeg git',
        'pip install --upgrade pip',
        'pip install yt-dlp mutagen',
        'termux-setup-storage'
    ]
    
    for cmd in commands:
        try:
            print(f"Executando: {cmd}")
            subprocess.run(cmd, shell=True, check=True)
        except Exception as e:
            print(f"‚ö†Ô∏è Erro ao executar {cmd}: {str(e)}")
            return False
    return True

def main():
    # Verifica e instala depend√™ncias
    if not install_dependencies():
        print("‚ùå Falha na instala√ß√£o das depend√™ncias")
        sys.exit(1)

    # Agora importa os m√≥dulos ap√≥s instala√ß√£o
    try:
        import yt_dlp
        from mutagen.id3 import ID3, TIT2, TPE1, TALB, APIC
        from mutagen.mp3 import MP3
        from urllib.request import urlopen
    except ImportError as e:
        print(f"‚ùå Erro cr√≠tico: {str(e)}")
        sys.exit(1)

    # Configura√ß√µes principais
    FFMPEG_PATH = "/data/data/com.termux/files/usr/bin/ffmpeg"
    MUSIC_FOLDER = "/storage/emulated/0/Music"

    def create_music_folder():
        """Cria a pasta de m√∫sica se n√£o existir"""
        os.makedirs(MUSIC_FOLDER, exist_ok=True)
        return MUSIC_FOLDER

    def download_and_organize():
        """Fun√ß√£o principal de download"""
        url = input("\nüéµ Cole a URL do YouTube/YouTube Music: ").strip()
        
        if not url.startswith(('http://', 'https://')):
            print("\n‚ö†Ô∏è URL inv√°lida! Deve come√ßar com http:// ou https://")
            return

        music_path = create_music_folder()
        print(f"\nüìÅ Pasta de destino: {music_path}")

        ydl_opts = {
            'ffmpeg_location': FFMPEG_PATH,
            'format': 'bestaudio/best',
            'outtmpl': os.path.join(music_path, '%(title)s.%(ext)s'),
            'postprocessors': [
                {
                    'key': 'FFmpegExtractAudio',
                    'preferredcodec': 'mp3',
                    'preferredquality': '320',
                },
                {
                    'key': 'FFmpegMetadata',
                    'add_metadata': True,
                },
                {
                    'key': 'EmbedThumbnail',
                }
            ],
            'writethumbnail': True,
            'quiet': False,
        }

        try:
            print("\n‚¨áÔ∏è Baixando... (Isso pode levar alguns minutos)")
            with yt_dlp.YoutubeDL(ydl_opts) as ydl:
                ydl.download([url])
            print("\n‚úÖ Download conclu√≠do com sucesso!")
            print(f"üéß M√∫sica salva em: {music_path}")
        except Exception as e:
            print(f"\n‚ùå Erro durante o download: {str(e)}")

    # Interface do usu√°rio
    print("\n=== YouTube Music Downloader ===")
    print("üìå Funcionalidades:")
    print("- Baixa m√∫sicas do YouTube e YouTube Music")
    print("- Converte para MP3 320kbps")
    print("- Adiciona metadados e capa automaticamente")
    print("="*50)
    
    download_and_organize()
    input("\nPressione Enter para sair...")

if __name__ == "__main__":
    main()
